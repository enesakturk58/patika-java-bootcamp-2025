package com.patika.enesakturk.week01.cohort._1_practices;

public class SwapNumbersExample {
    public static void main(String[] args) {

        int a = 5;
        int b = 10;

        System.out.println("a:" + a + " - " + "b:" + b);

        a = a+b;
        System.out.println("a:" + a + " - " + "b:" + b);
        b = a-b;
        System.out.println("a:" + a + " - " + "b:" + b);
        a = a-b;
        System.out.println("a:" + a + " - " + "b:" + b);



    }
}
